<h1>Quem Somos</h1>
<p>A Simas Turbo foi fundada em 2012, em João Pessoa, por Simas Alves, 
    um mecânico apaixonado por carros e pela arte de consertar e melhorar veículos. 
    Desde pequeno, Simas sonhava em ter sua própria oficina, onde pudesse oferecer serviços de alta qualidade e atendimento
     personalizado.</p>

<p>Com uma pequena economia e uma paixão imensa por motores, ele abriu as portas da Simas Turbo em um modesto galpão na cidade. 
    O sucesso não demorou a chegar. Com seu compromisso em oferecer serviços excepcionais e honestidade, a oficina rapidamente
     ganhou a confiança dos moradores.</p>

<p>Em 2015, devido ao aumento de clientes e à reputação crescente, Simas decidiu abrir uma segunda 
    loja em Campina Grande, permitindo que mais pessoas tivessem acesso aos serviços de qualidade que a Simas Turbo oferecia.</p>

<p>A Simas Turbo sempre se destacou pela variedade de serviços oferecidos. Além de manutenções
     comuns, como troca de óleo e freios, a oficina se especializou em serviços de performance automotiva, atraindo 
     entusiastas de carros que buscavam melhorias em seus veículos.</p>

<p>Simas não apenas queria ter uma oficina de sucesso; ele queria fazer a diferença na comunidade. 
    A Simas Turbo se envolve em diversas iniciativas locais, incluindo parcerias com escolas para promover a 
    educação em mecânica, e projetos sociais que oferecem serviços gratuitos a comunidades carentes.</p>

<p>Hoje, a Simas Turbo é reconhecida como uma das melhores oficinas mecânicas da Paraíba. Com um forte foco na qualidade
     do serviço e na satisfação do cliente, a oficina continua a crescer e evoluir. Simas sonha em expandir ainda mais, talvez 
     abrindo novas filiais em outras cidades.</p>

<h3>Slogan</h3>
<p><em>"Simas Turbo: Acelerando a confiança no seu carro!"</em></p>
